public class Exercicio4 {
    public static void main(String[] args) {
        System.out.println("Numbers in ascending order: ");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println("Numbers in descending order: ");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
